a, b, c = map(int, input().split())

# a**b % c -> b번 연산
# 2^32 -> 32번 연산 = 2^16 * 2^16
# (2^16)^2 -> 17번 연산 = 2^8
# ((2^8)^2)^2 ->10번 연산
# 2^2^2^2^2^2 -> 6번 연

# 10**11
# 10^5^2*10 = 10^5 * 10^5 * 10
# 10^2^2^2*10^3 = 10^2 * 10^2 * 10 * 10^2 * 10^2 * 10 * 10
# 10^2^2^2*10^2*10
def sol(a, b, c):
    if b > 2:
        return sol(a, b//2, c)**2 * sol(a, b%2, c) % c
    elif b == 2:
        return a**2
    elif b == 1:
        return a
    else:
        return 1

print(sol(a,b,c) % c)